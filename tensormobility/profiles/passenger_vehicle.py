"""Coupled passenger-behavior and vehicle-service layers for STB-FTT.

The module implements a deliberately bounded multimodal extension of the
existing typed sparse prototype. Passenger activity columns carry person mass;
vehicle route columns carry vehicle trips and provide seat/service capacity.
The two layers are coupled by an explicit request-capacity inequality

    R x <= S y,

rather than by an implicit occupancy coefficient. Road congestion and queues
are generated by private and service vehicles, while passengers experience
travel time, waiting, transfers, price, and schedule penalties.

The implementation is a research harness, not a full fleet-dispatch system.
Shared-vehicle routes are continuous set-covering columns over aggregate
request classes, and the internal temporal backend remains a point queue
without spillback.
"""

