@dataclass(frozen=True)
class PassengerVehicleEquilibriumResult:
    model: PassengerVehicleModel
    passenger: PassengerChoiceResult
    vehicle: VehicleColumnGenerationResult
    road_link_vehicle_flow: np.ndarray
    link_cost: np.ndarray
    queue: SparsePathQueueResult
    fixed_point_residual: float
    iterations: int
    history: pd.DataFrame
    dynamic_path_time: dict[tuple[tuple[int, ...], int], float]


def _private_link_flow(network: StaticNetwork, flows: Mapping[tuple[tuple[int, ...], int], float]) -> np.ndarray:
    link = np.zeros(network.n_links, dtype=float)
    for (path, _), amount in flows.items():
        if path and amount > 0:
            link[np.asarray(path, dtype=int)] += float(amount)
    return link


def _aggregate_dynamic_paths(
    model: PassengerVehicleModel,
    passenger: PassengerChoiceResult,
    vehicle: VehicleColumnGenerationResult,
) -> tuple[list[tuple[int, ...]], np.ndarray]:
    flow: defaultdict[tuple[tuple[int, ...], int], float] = defaultdict(float)
    for key, amount in passenger.private_vehicle_path_departure.items():
        flow[key] += float(amount)
    for row in vehicle.pattern_table.itertuples(index=False):
        amount = float(row.vehicle_flow)
        path = _parse_path(row.link_index_sequence)
        if amount > 0 and path:
            flow[(path, int(row.departure_index))] += amount
    paths = sorted({path for path, _ in flow}, key=lambda p: (len(p), p))
    path_index = {path: i for i, path in enumerate(paths)}
    departures = np.zeros((len(paths), model.n_departures), dtype=float)
    for (path, departure), amount in flow.items():
        if 0 <= departure < model.n_departures:
            departures[path_index[path], departure] += float(amount)
    return paths, departures


def _queue_time_map(
    paths: Sequence[tuple[int, ...]],
    queue: SparsePathQueueResult,
) -> dict[tuple[tuple[int, ...], int], float]:
    result: dict[tuple[tuple[int, ...], int], float] = {}
    for p, path in enumerate(paths):
        for t in range(queue.path_departure_travel_time.shape[1]):
            result[(tuple(path), t)] = float(queue.path_departure_travel_time[p, t])
    return result


def solve_passenger_vehicle_equilibrium(
    model: PassengerVehicleModel,
    *,
    parameters: PassengerBehaviorParameters = PassengerBehaviorParameters(),
    max_iterations: int = 35,
    damping: float = 0.45,
    tolerance: float = 2e-3,
    dynamic_feedback: bool = True,
    queue_every: int = 3,
    queue_horizon_steps: int = 120,
    dynamic_damping: float = 0.08,
    capacity_multiplier: np.ndarray | float = 1.0,
    vehicle_solver_options: Mapping[str, object] | None = None,
    passenger_price_rule: str = "average_cost",
) -> PassengerVehicleEquilibriumResult:
    network = model.case.network
    link_cost = network.free_flow_time.copy()
    price = {str(v): 0.0 for v in model.request_template["request_id"]}
    dynamic_map: dict[tuple[tuple[int, ...], int], float] = {}
    rows: list[dict[str, float | int]] = []
    final_passenger: PassengerChoiceResult | None = None
    final_vehicle: VehicleColumnGenerationResult | None = None
    final_queue: SparsePathQueueResult | None = None
    residual = np.inf

    for iteration in range(1, max_iterations + 1):
        passenger = evaluate_passenger_choice(
            model,
            link_cost,
            price,
            parameters=parameters,
            dynamic_time_override=dynamic_map if dynamic_map else None,
        )
        vehicle = solve_vehicle_service_column_generation(
            network,
            passenger.service_request_table,
            link_cost,
            **({} if vehicle_solver_options is None else dict(vehicle_solver_options)),
        )
        private_link = _private_link_flow(network, passenger.private_vehicle_path_departure)
        road_flow = private_link + vehicle.road_link_flow
        target_link_cost = bpr_cost(road_flow, network.free_flow_time, network.capacity)
        if passenger_price_rule == "dual":
            price_series = vehicle.request_dual_price
        elif passenger_price_rule == "average_cost":
            price_series = vehicle.average_service_price
        else:
            raise ValueError("passenger_price_rule must be 'dual' or 'average_cost'")
        target_price = {str(k): float(v) for k, v in price_series.items()}
        cost_residual = float(np.max(np.abs(target_link_cost - link_cost) / np.maximum(link_cost, 1.0)))
        price_residual_absolute = float(max((abs(target_price[k] - price.get(k, 0.0)) for k in target_price), default=0.0))
        price_residual = float(max((
            abs(target_price[k] - price.get(k, 0.0)) / max(abs(target_price[k]), abs(price.get(k, 0.0)), 1.0)
            for k in target_price
        ), default=0.0))
        residual = max(cost_residual, price_residual, passenger.mass_residual, vehicle.demand_residual)

        if dynamic_feedback and iteration % max(queue_every, 1) == 0:
            paths, departures = _aggregate_dynamic_paths(model, passenger, vehicle)
            queue = run_sparse_path_point_queue(
                network,
                paths,
                departures,
                dt_minutes=5.0,
                horizon_steps=queue_horizon_steps,
                capacity_multiplier=capacity_multiplier,
            )
            target_dynamic = _queue_time_map(paths, queue)
            if dynamic_map:
                keys = set(dynamic_map) | set(target_dynamic)
                dynamic_residual = max(
                    (
                        abs(target_dynamic.get(k, dynamic_map.get(k, 0.0)) - dynamic_map.get(k, target_dynamic.get(k, 0.0)))
                        / max(abs(dynamic_map.get(k, target_dynamic.get(k, 1.0))), 1.0)
                        for k in keys
                    ),
                    default=0.0,
                )
                residual = max(residual, float(dynamic_residual))
                dynamic_map = {
                    k: (1.0 - dynamic_damping) * dynamic_map.get(k, v) + dynamic_damping * v
                    for k, v in target_dynamic.items()
                }
            else:
                dynamic_map = target_dynamic
            final_queue = queue

        rows.append({
            "iteration": iteration,
            "fixed_point_residual": residual,
            "cost_residual": cost_residual,
            "service_price_residual": price_residual,
            "service_price_residual_absolute": price_residual_absolute,
            "passenger_mass_residual": passenger.mass_residual,
            "vehicle_service_shortfall": vehicle.demand_residual,
            "vehicle_pricing_gap": vehicle.relative_pricing_gap,
            "private_vehicle_trips": passenger.total_private_vehicle_trips,
            "service_vehicle_trips": float(vehicle.pattern_table["vehicle_flow"].sum()),
            "road_vehicle_flow_sum": float(road_flow.sum()),
        })
        link_cost = (1.0 - damping) * link_cost + damping * target_link_cost
        price = {
            k: (1.0 - damping) * price.get(k, 0.0) + damping * target_price.get(k, 0.0)
            for k in set(price) | set(target_price)
        }
        final_passenger, final_vehicle = passenger, vehicle
        if iteration >= 6 and residual <= tolerance:
            break

    if final_passenger is None or final_vehicle is None:
        raise RuntimeError("coupled solve did not execute")
    private_link = _private_link_flow(network, final_passenger.private_vehicle_path_departure)
    road_flow = private_link + final_vehicle.road_link_flow
    paths, departures = _aggregate_dynamic_paths(model, final_passenger, final_vehicle)
    final_queue = run_sparse_path_point_queue(
        network,
        paths,
        departures,
        dt_minutes=5.0,
        horizon_steps=queue_horizon_steps,
        capacity_multiplier=capacity_multiplier,
    )
    dynamic_map = _queue_time_map(paths, final_queue)
    return PassengerVehicleEquilibriumResult(
        model=model,
        passenger=final_passenger,
        vehicle=final_vehicle,
        road_link_vehicle_flow=road_flow,
        link_cost=link_cost,
        queue=final_queue,
        fixed_point_residual=float(residual),
        iterations=len(rows),
        history=pd.DataFrame(rows),
        dynamic_path_time=dynamic_map,
    )


