@dataclass(frozen=True)
class PassengerVehicleAnalyticalResult:
    demand: float
    analytical_auto_flow: float
    analytical_shared_flow: float
    analytical_vehicle_flow: float
    analytical_service_price: float
    numerical_auto_flow: float
    numerical_shared_flow: float
    numerical_vehicle_flow: float
    numerical_service_price: float
    max_flow_error: float
    price_error: float
    iterations: int
    history: pd.DataFrame


def solve_analytical_passenger_vehicle_market(
    *,
    demand: float = 1000.0,
    auto_base_cost: float = 7.0,
    auto_congestion_slope: float = 0.006,
    shared_base_cost: float = 8.8,
    pooled_vehicle_cost: float = 7.2,
    pooled_capacity: float = 2.0,
    entropy_temperature: float = 1.4,
    damping: float = 0.55,
    tolerance: float = 1e-11,
    max_iterations: int = 500,
) -> PassengerVehicleAnalyticalResult:
    """Verify passenger choice + vehicle capacity against a scalar KKT root.

    The joint convex anchor is

        min integral c_auto(x_a) dx_a + c_s x_s + k y
            + tau * sum_m x_m(log x_m - 1)
        s.t. x_a + x_s = d,  x_s <= q y,  x,y >= 0.

    Positive vehicle cost makes the service constraint bind, so the exact
    service price is k/q and y=x_s/q. The remaining passenger KKT condition is
    a one-dimensional root solved independently with Brent's method.
    """
    if demand <= 0 or pooled_capacity <= 0 or entropy_temperature <= 0:
        raise ValueError("demand, capacity, and temperature must be positive")
    service_price = pooled_vehicle_cost / pooled_capacity

    def stationarity(auto_flow: float) -> float:
        shared_flow = demand - auto_flow
        return (
            auto_base_cost
            + auto_congestion_slope * auto_flow
            + entropy_temperature * math.log(auto_flow)
            - shared_base_cost
            - service_price
            - entropy_temperature * math.log(shared_flow)
        )

    analytical_auto = float(brentq(stationarity, 1e-10, demand - 1e-10, xtol=1e-13, rtol=1e-13))
    analytical_shared = demand - analytical_auto
    analytical_vehicle = analytical_shared / pooled_capacity

    auto = demand / 2.0
    price = 0.0
    rows: list[dict[str, float | int]] = []
    for iteration in range(1, max_iterations + 1):
        auto_cost = auto_base_cost + auto_congestion_slope * auto
        # Entropic passenger response for the current vehicle-market price.
        utility = np.asarray((-auto_cost, -(shared_base_cost + price)), dtype=float) / entropy_temperature
        utility -= utility.max()
        probability = np.exp(utility)
        probability /= probability.sum()
        target_auto = demand * float(probability[0])
        target_shared = demand - target_auto
        # The homogeneous vehicle LP has a pooled column with q seats and cost k.
        target_vehicle = target_shared / pooled_capacity
        target_price = service_price if target_shared > 1e-12 else 0.0
        residual = max(abs(target_auto - auto) / demand, abs(target_price - price) / max(service_price, 1.0))
        rows.append({
            "iteration": iteration,
            "auto_flow": auto,
            "shared_flow": demand - auto,
            "vehicle_flow": (demand - auto) / pooled_capacity,
            "service_price": price,
            "fixed_point_residual": residual,
        })
        auto = (1.0 - damping) * auto + damping * target_auto
        price = (1.0 - damping) * price + damping * target_price
        if residual <= tolerance:
            break

    numerical_shared = demand - auto
    numerical_vehicle = numerical_shared / pooled_capacity
    return PassengerVehicleAnalyticalResult(
        demand=float(demand),
        analytical_auto_flow=analytical_auto,
        analytical_shared_flow=analytical_shared,
        analytical_vehicle_flow=analytical_vehicle,
        analytical_service_price=service_price,
        numerical_auto_flow=float(auto),
        numerical_shared_flow=float(numerical_shared),
        numerical_vehicle_flow=float(numerical_vehicle),
        numerical_service_price=float(price),
        max_flow_error=float(max(abs(auto - analytical_auto), abs(numerical_shared - analytical_shared))),
        price_error=float(abs(price - service_price)),
        iterations=len(rows),
        history=pd.DataFrame(rows),
    )


