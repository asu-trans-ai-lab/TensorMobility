def build_passenger_vehicle_coupling_operators(
    model: PassengerVehicleModel,
    result: VehicleColumnGenerationResult,
) -> tuple[TypedOperator, TypedOperator]:
    """Return R(request <- passenger column) and S(request <- vehicle pattern)."""
    pattern_axis = Axis(
        "vehicle_service_pattern",
        tuple(result.pattern_table["pattern_id"].astype(str)),
        "shared-vehicle and bus service route columns",
    )
    request_id_to_pos = {str(v): i for i, v in enumerate(model.request_template["request_id"])}
    rows: list[int] = []
    cols: list[int] = []
    values: list[float] = []
    for j, row in enumerate(result.pattern_table.itertuples(index=False)):
        ids = [v for v in str(row.served_request_ids).split(";") if v]
        caps = [float(v) for v in str(row.served_capacities).split(";") if v]
        for request_id, capacity in zip(ids, caps, strict=True):
            rows.append(request_id_to_pos[request_id])
            cols.append(j)
            values.append(capacity)
    supply = sparse_operator_from_triplets(
        name="vehicle_pattern_to_service_capacity",
        source=pattern_axis,
        target=model.request_axis,
        rows=rows,
        cols=cols,
        values=values,
        input_measure="vehicle_flow",
        output_measure="person_flow",
        conservation="none",
        description="Converts vehicle-route flows to provided passenger-seat/service capacity.",
    )
    return model.column_to_request, supply


def export_passenger_vehicle_results(
    result: PassengerVehicleEquilibriumResult,
    output_dir: str | "os.PathLike[str]",
) -> None:
    from pathlib import Path
    import json

    output = Path(output_dir)
    output.mkdir(parents=True, exist_ok=True)
    result.model.groups.to_csv(output / "passenger_group.csv", index=False)
    table = result.passenger.column_table.copy()
    for field in ("road_path", "private_vehicle_path", "bus_segment_path"):
        table[field] = table[field].map(lambda p: ";".join(map(str, tuple(p))))
    table.to_csv(output / "passenger_column_flow.csv", index=False)
    result.passenger.mode_share.to_csv(output / "passenger_mode_share.csv", index=False)
    result.passenger.departure_share.to_csv(output / "passenger_departure_share.csv", index=False)
    result.passenger.activity_mode_share.to_csv(output / "activity_mode_share.csv", index=False)
    request = result.passenger.service_request_table.copy()
    request["service_dual_price"] = request["request_id"].map(result.vehicle.request_dual_price).fillna(0.0)
    request["average_service_price"] = request["request_id"].map(result.vehicle.average_service_price).fillna(0.0)
    request["supplied_capacity"] = result.vehicle.supplied_capacity
    request["capacity_slack"] = request["supplied_capacity"] - request["demand"]
    request.to_csv(output / "service_request_flow.csv", index=False)
    result.vehicle.pattern_table.to_csv(output / "vehicle_route_flow.csv", index=False)

    # GMNS/TAPLite-style multimodal aliases with explicit flow measures.
    od_flow = (
        table.groupby("od_id", as_index=False)["person_flow"].sum()
        .rename(columns={"person_flow": "flow"})
    )
    od_flow["flow_measure"] = "person_flow"
    od_flow.to_csv(output / "od_flow.csv", index=False)

    route_flow = (
        table.groupby(["od_id", "mode", "departure_index", "road_path"], as_index=False)["person_flow"].sum()
        .rename(columns={"person_flow": "flow", "road_path": "link_index_sequence"})
    )
    route_flow.insert(0, "route_id", [f"passenger_route_{i}" for i in range(len(route_flow))])
    route_flow["flow_measure"] = "person_flow"
    route_flow.to_csv(output / "route_flow.csv", index=False)

    vehicle_path_rows: list[dict[str, object]] = []
    for i, ((path, departure), amount) in enumerate(sorted(result.passenger.private_vehicle_path_departure.items())):
        vehicle_path_rows.append({
            "path_id": f"private_{i}",
            "source_type": "private_passenger_column",
            "service_mode": "private_auto",
            "departure_index": int(departure),
            "link_index_sequence": ";".join(map(str, path)),
            "flow": float(amount),
            "flow_measure": "vehicle_flow",
        })
    for row in result.vehicle.pattern_table.itertuples(index=False):
        amount = float(row.vehicle_flow)
        if amount <= 1e-12:
            continue
        vehicle_path_rows.append({
            "path_id": str(row.pattern_id),
            "source_type": "service_vehicle_pattern",
            "service_mode": str(row.service_mode),
            "departure_index": int(row.departure_index),
            "link_index_sequence": str(row.link_index_sequence),
            "flow": amount,
            "flow_measure": "vehicle_flow",
        })
    vehicle_path = pd.DataFrame(vehicle_path_rows)
    vehicle_path.to_csv(output / "path_flow.csv", index=False)
    vehicle_path.to_csv(output / "vehicle_path_departure_flow.csv", index=False)
    if vehicle_path.empty:
        departure_profile = pd.DataFrame(columns=["departure_index", "vehicle_flow", "share"])
    else:
        departure_profile = vehicle_path.groupby("departure_index", as_index=False)["flow"].sum().rename(columns={"flow": "vehicle_flow"})
        departure_profile["share"] = departure_profile["vehicle_flow"] / max(float(departure_profile["vehicle_flow"].sum()), 1e-12)
    departure_profile.to_csv(output / "departure_profile.csv", index=False)

    link_table = pd.DataFrame({
        "link_id": result.model.case.network.links["link_id"].astype(str),
        "flow": result.road_link_vehicle_flow,
        "flow_measure": "vehicle_flow",
        "travel_time": result.link_cost,
        "free_flow_time": result.model.case.network.free_flow_time,
        "capacity": result.model.case.network.capacity,
    })
    link_table.to_csv(output / "link_flow.csv", index=False)

    result.vehicle.history.to_csv(output / "vehicle_column_generation_history.csv", index=False)
    result.history.to_csv(output / "passenger_vehicle_fixed_point_history.csv", index=False)
    pd.DataFrame({
        "link_id": result.model.case.network.links["link_id"].astype(str),
        "vehicle_flow": result.road_link_vehicle_flow,
        "travel_time": result.link_cost,
        "free_flow_time": result.model.case.network.free_flow_time,
        "capacity": result.model.case.network.capacity,
    }).to_csv(output / "road_link_vehicle_flow.csv", index=False)
    result.queue.history.to_csv(output / "multimodal_queue_history.csv", index=False)
    request_operator, supply_operator = build_passenger_vehicle_coupling_operators(result.model, result.vehicle)
    coupling_rows: list[dict[str, object]] = []
    request_matrix = request_operator.matrix.tocoo()
    for r, c, value in zip(request_matrix.row, request_matrix.col, request_matrix.data, strict=True):
        coupling_rows.append({
            "coupling_type": "passenger_request",
            "request_id": result.model.request_axis.labels[int(r)],
            "column_id": result.model.column_axis.labels[int(c)],
            "coefficient": float(value),
        })
    supply_matrix = supply_operator.matrix.tocoo()
    for r, c, value in zip(supply_matrix.row, supply_matrix.col, supply_matrix.data, strict=True):
        coupling_rows.append({
            "coupling_type": "vehicle_capacity",
            "request_id": result.model.request_axis.labels[int(r)],
            "column_id": supply_operator.source.labels[int(c)],
            "coefficient": float(value),
        })
    pd.DataFrame(coupling_rows).to_csv(output / "passenger_vehicle_coupling.csv", index=False)
    manifest = {
        "framework": "STB-FTT passenger-vehicle multimodal extension",
        "certificate": "passenger mass + vehicle service feasibility + enumerated vehicle pricing + queue conservation",
        "fixed_point_residual": result.fixed_point_residual,
        "passenger_mass_residual": result.passenger.mass_residual,
        "vehicle_service_shortfall": result.vehicle.demand_residual,
        "vehicle_pricing_gap": result.vehicle.relative_pricing_gap,
        "queue_conservation_residual": float(np.max(np.abs(result.queue.conservation_residual))),
        "spillback": False,
    }
    (output / "passenger_vehicle_manifest.json").write_text(json.dumps(manifest, indent=2), encoding="utf-8")
