def _parse_path(value: object) -> tuple[int, ...]:
    if isinstance(value, tuple):
        return tuple(int(v) for v in value)
    if isinstance(value, list):
        return tuple(int(v) for v in value)
    text = str(value).strip()
    return tuple(int(v) for v in text.split(";") if str(v).strip()) if text else tuple()


def _select_od_routes(assignment: ColumnGenerationResult, max_paths: int) -> dict[str, list[tuple[int, ...]]]:
    routes: dict[str, list[tuple[int, ...]]] = {}
    table = assignment.path_table.copy()
    if table.empty:
        return routes
    table = table.sort_values(["od_id", "path_flow", "path_cost"], ascending=[True, False, True])
    for od_id, group in table.groupby("od_id", sort=False):
        unique: list[tuple[int, ...]] = []
        for value in group["link_index_sequence"]:
            path = _parse_path(value)
            if path and path not in unique:
                unique.append(path)
            if len(unique) >= max_paths:
                break
        routes[str(od_id)] = unique
    return routes


def _grid_bus_line(case: GridCase) -> tuple[dict[int, int], dict[str, tuple[int, ...]], tuple[int, ...]]:
    row = case.rows // 2
    stop_by_column = {c: row * case.columns + c + 1 for c in range(case.columns)}
    east_nodes = tuple(stop_by_column[c] for c in range(case.columns))
    west_nodes = tuple(reversed(east_nodes))
    east_links: list[int] = []
    west_links: list[int] = []
    for nodes, target in ((east_nodes, east_links), (west_nodes, west_links)):
        for u, v in zip(nodes[:-1], nodes[1:], strict=True):
            path, _ = case.network.shortest_path(u, v, case.network.free_flow_time)
            target.extend(path)
    return stop_by_column, {"E": tuple(east_links), "W": tuple(west_links)}, east_nodes


