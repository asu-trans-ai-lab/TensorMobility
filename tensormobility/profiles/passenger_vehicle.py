def _path_cost(path: Sequence[int], link_cost: np.ndarray) -> float:
    return float(link_cost[np.asarray(path, dtype=int)].sum()) if path else 0.0


def _concat_paths(*segments: Sequence[int]) -> tuple[int, ...]:
    out: list[int] = []
    for segment in segments:
        out.extend(int(v) for v in segment)
    return tuple(out)


def _node_xy(network: StaticNetwork) -> dict[int, tuple[float, float]]:
    x = network.nodes.get("x_coord", pd.Series(np.arange(network.n_nodes))).to_numpy(float)
    y = network.nodes.get("y_coord", pd.Series(np.zeros(network.n_nodes))).to_numpy(float)
    return {int(node): (float(xx), float(yy)) for node, xx, yy in zip(network.nodes["node_id"], x, y, strict=True)}


def _manhattan(xy: Mapping[int, tuple[float, float]], a: int, b: int) -> float:
    xa, ya = xy[int(a)]
    xb, yb = xy[int(b)]
    return abs(xa - xb) + abs(ya - yb)


def _best_pooled_route(
    network: StaticNetwork,
    request_a: pd.Series,
    request_b: pd.Series,
    link_cost: np.ndarray,
) -> tuple[tuple[int, ...], float]:
    """Return the least-cost pickup-before-dropoff order for two requests."""
    oa, da = int(request_a.origin_node_id), int(request_a.destination_node_id)
    ob, db = int(request_b.origin_node_id), int(request_b.destination_node_id)
    node_orders = (
        (oa, ob, da, db),
        (oa, ob, db, da),
        (ob, oa, da, db),
        (ob, oa, db, da),
    )
    best_path: tuple[int, ...] | None = None
    best_cost = np.inf
    for order in node_orders:
        segments: list[tuple[int, ...]] = []
        total = 0.0
        feasible = True
        for u, v in zip(order[:-1], order[1:], strict=True):
            if u == v:
                segments.append(tuple())
                continue
            try:
                path, cost = network.shortest_path(u, v, link_cost)
            except RuntimeError:
                feasible = False
                break
            segments.append(path)
            total += cost
        if feasible and total < best_cost:
            best_cost = float(total)
            best_path = _concat_paths(*segments)
    if best_path is None:
        raise RuntimeError("no feasible pooled route")
    return best_path, best_cost


def _build_vehicle_pattern_pool(
    network: StaticNetwork,
    request_table: pd.DataFrame,
    link_cost: np.ndarray,
    *,
    shared_capacity: float,
    bus_capacity: float,
    pickup_overhead: float,
    pooled_overhead: float,
    bus_dispatch_cost: float,
    max_pair_distance: float,
    pair_neighbours: int,
) -> tuple[list[VehiclePattern], list[VehiclePattern]]:
    """Build mandatory initial columns and compatible pooled candidates."""
    requests = request_table.reset_index(drop=True)
    xy = _node_xy(network)
    initial: list[VehiclePattern] = []
    candidates: list[VehiclePattern] = []

    shared_indices = requests.index[requests["service_mode"] == "shared_ride"].tolist()
    for r in shared_indices:
        row = requests.iloc[r]
        path, travel_time = network.shortest_path(int(row.origin_node_id), int(row.destination_node_id), link_cost)
        initial.append(VehiclePattern(
            pattern_id=f"shared_direct:{row.request_id}",
            service_mode="shared_ride",
            departure_index=int(row.departure_index),
            pattern_type="direct",
            served_request_indices=(int(r),),
            served_capacities=(1.0,),
            link_sequence=path,
            operating_cost=float(travel_time + pickup_overhead),
            free_flow_time=float(_path_cost(path, network.free_flow_time)),
        ))
        candidates.append(VehiclePattern(
            pattern_id=f"shared_same_pool:{row.request_id}",
            service_mode="shared_ride",
            departure_index=int(row.departure_index),
            pattern_type="same_request_pool",
            served_request_indices=(int(r),),
            served_capacities=(float(shared_capacity),),
            link_sequence=path,
            operating_cost=float(1.8 * travel_time + pooled_overhead),
            free_flow_time=float(_path_cost(path, network.free_flow_time)),
        ))

    # Bus request rows contain the full directional service path as a semicolon
    # separated link-index sequence. One bus-run column provides many seats.
    for r in requests.index[requests["service_mode"] == "bus"].tolist():
        row = requests.iloc[r]
        text = str(row.link_index_sequence)
        path = tuple(int(v) for v in text.split(";") if str(v).strip())
        initial.append(VehiclePattern(
            pattern_id=f"bus_run:{row.request_id}",
            service_mode="bus",
            departure_index=int(row.departure_index),
            pattern_type="scheduled_run",
            served_request_indices=(int(r),),
            served_capacities=(float(bus_capacity),),
            link_sequence=path,
            operating_cost=float(_path_cost(path, link_cost) + bus_dispatch_cost),
            free_flow_time=float(_path_cost(path, network.free_flow_time)),
        ))

    # Spatially and temporally compatible shared request classes form pooled
    # candidate columns. Neighbour filtering avoids an O(R^2) route explosion.
    for r in shared_indices:
        row_r = requests.iloc[r]
        alternatives: list[tuple[float, int]] = []
        for s in shared_indices:
            if s <= r:
                continue
            row_s = requests.iloc[s]
            if int(row_s.departure_index) != int(row_r.departure_index):
                continue
            distance = _manhattan(xy, int(row_r.origin_node_id), int(row_s.origin_node_id)) + _manhattan(
                xy, int(row_r.destination_node_id), int(row_s.destination_node_id)
            )
            if distance <= max_pair_distance:
                alternatives.append((distance, int(s)))
        alternatives.sort()
        for _, s in alternatives[:pair_neighbours]:
            row_s = requests.iloc[s]
            path, travel_time = _best_pooled_route(network, row_r, row_s, link_cost)
            candidates.append(VehiclePattern(
                pattern_id=f"shared_pair:{row_r.request_id}|{row_s.request_id}",
                service_mode="shared_ride",
                departure_index=int(row_r.departure_index),
                pattern_type="paired_requests",
                served_request_indices=(int(r), int(s)),
                served_capacities=(1.0, 1.0),
                link_sequence=path,
                operating_cost=float(travel_time + pooled_overhead),
                free_flow_time=float(_path_cost(path, network.free_flow_time)),
            ))
    return initial, candidates


def _solve_vehicle_master(
    patterns: Sequence[VehiclePattern],
    demand: np.ndarray,
    n_requests: int,
) -> tuple[np.ndarray, np.ndarray, np.ndarray, float]:
    if not patterns:
        if np.max(demand, initial=0.0) > 1e-12:
            raise RuntimeError("vehicle master has demand but no columns")
        return np.zeros(0), np.zeros(n_requests), np.zeros(n_requests), 0.0
    rows: list[int] = []
    cols: list[int] = []
    data: list[float] = []
    cost = np.zeros(len(patterns), dtype=float)
    for j, pattern in enumerate(patterns):
        cost[j] = pattern.operating_cost
        for r, capacity in zip(pattern.served_request_indices, pattern.served_capacities, strict=True):
            rows.append(int(r))
            cols.append(j)
            data.append(float(capacity))
    coverage = sparse.coo_matrix((data, (rows, cols)), shape=(n_requests, len(patterns))).tocsr()
    result = linprog(
        c=cost,
        A_ub=-coverage,
        b_ub=-demand,
        bounds=(0.0, None),
        method="highs",
    )
    if not result.success:
        raise RuntimeError(f"vehicle restricted master failed: {result.message}")
    y = np.asarray(result.x, dtype=float)
    supplied = np.asarray(coverage @ y).ravel()
    # For -Qy <= -d, HiGHS marginals are non-positive; negate them to
    # obtain nonnegative marginal service prices.
    dual = -np.asarray(result.ineqlin.marginals, dtype=float)
    residual = float(np.max(np.maximum(demand - supplied, 0.0), initial=0.0))
    return y, supplied, dual, residual


def solve_vehicle_service_column_generation(
    network: StaticNetwork,
    request_table: pd.DataFrame,
    link_cost: np.ndarray,
    *,
    shared_capacity: float = 2.0,
    bus_capacity: float = 40.0,
    pickup_overhead: float = 1.4,
    pooled_overhead: float = 2.0,
    bus_dispatch_cost: float = 8.0,
    max_pair_distance: float = 5.0,
    pair_neighbours: int = 5,
    max_iterations: int = 20,
    pricing_tolerance: float = 1e-8,
    add_per_iteration: int = 20,
) -> VehicleColumnGenerationResult:
    required = {
        "request_id",
        "service_mode",
        "departure_index",
        "origin_node_id",
        "destination_node_id",
        "demand",
        "link_index_sequence",
    }
    missing = required - set(request_table.columns)
    if missing:
        raise ValueError(f"request_table missing fields {missing}")
    requests = request_table.copy().reset_index(drop=True)
    demand = requests["demand"].to_numpy(float)
    if np.any(demand < -1e-10) or np.any(~np.isfinite(demand)):
        raise ValueError("service demand must be finite and nonnegative")
    costs = np.asarray(link_cost, dtype=float)
    if costs.shape != (network.n_links,):
        raise ValueError("link_cost has wrong shape")

    initial, candidates = _build_vehicle_pattern_pool(
        network,
        requests,
        costs,
        shared_capacity=shared_capacity,
        bus_capacity=bus_capacity,
        pickup_overhead=pickup_overhead,
        pooled_overhead=pooled_overhead,
        bus_dispatch_cost=bus_dispatch_cost,
        max_pair_distance=max_pair_distance,
        pair_neighbours=pair_neighbours,
    )
    active = list(initial)
    active_ids = {pattern.pattern_id for pattern in active}
    rows: list[dict[str, float | int]] = []
    pricing_calls = 0
    min_reduced_cost = 0.0

    for iteration in range(1, max_iterations + 1):
        y, supplied, dual, residual = _solve_vehicle_master(active, demand, len(requests))
        priced: list[tuple[float, VehiclePattern]] = []
        for pattern in candidates:
            if pattern.pattern_id in active_ids:
                continue
            pricing_calls += 1
            value = sum(
                float(capacity) * float(dual[int(r)])
                for r, capacity in zip(pattern.served_request_indices, pattern.served_capacities, strict=True)
            )
            reduced_cost = float(pattern.operating_cost - value)
            if reduced_cost < -pricing_tolerance:
                priced.append((reduced_cost, pattern))
        priced.sort(key=lambda item: item[0])
        min_reduced_cost = float(priced[0][0]) if priced else 0.0
        rows.append({
            "iteration": iteration,
            "active_patterns": len(active),
            "positive_pattern_flows": int(np.count_nonzero(y > 1e-9)),
            "objective": float(sum(p.operating_cost * yy for p, yy in zip(active, y, strict=True))),
            "max_service_shortfall": residual,
            "minimum_candidate_reduced_cost": min_reduced_cost,
            "columns_added": min(len(priced), add_per_iteration),
        })
        if not priced:
            break
        for _, pattern in priced[:add_per_iteration]:
            active.append(pattern)
            active_ids.add(pattern.pattern_id)

    y, supplied, dual, residual = _solve_vehicle_master(active, demand, len(requests))
    road_flow = np.zeros(network.n_links, dtype=float)
    pattern_rows: list[dict[str, object]] = []
    for pattern, flow in zip(active, y, strict=True):
        if flow > 0 and pattern.link_sequence:
            road_flow[np.asarray(pattern.link_sequence, dtype=int)] += float(flow)
        pattern_rows.append({
            "pattern_id": pattern.pattern_id,
            "service_mode": pattern.service_mode,
            "departure_index": pattern.departure_index,
            "pattern_type": pattern.pattern_type,
            "served_request_indices": ";".join(map(str, pattern.served_request_indices)),
            "served_request_ids": ";".join(str(requests.iloc[r].request_id) for r in pattern.served_request_indices),
            "served_capacities": ";".join(f"{v:.8g}" for v in pattern.served_capacities),
            "vehicle_flow": float(flow),
            "operating_cost": pattern.operating_cost,
            "free_flow_time": pattern.free_flow_time,
            "link_index_sequence": ";".join(map(str, pattern.link_sequence)),
            "n_links": len(pattern.link_sequence),
        })
    pattern_table = pd.DataFrame(pattern_rows)
    average_numerator = np.zeros(len(requests), dtype=float)
    average_denominator = np.zeros(len(requests), dtype=float)
    for pattern, flow in zip(active, y, strict=True):
        if flow <= 0:
            continue
        total_capacity = max(float(sum(pattern.served_capacities)), 1e-12)
        unit_cost = float(pattern.operating_cost) / total_capacity
        for r, capacity in zip(pattern.served_request_indices, pattern.served_capacities, strict=True):
            served = float(flow) * float(capacity)
            average_numerator[int(r)] += served * unit_cost
            average_denominator[int(r)] += served
    average_price = np.divide(
        average_numerator,
        average_denominator,
        out=dual.copy(),
        where=average_denominator > 1e-12,
    )
    scale = max(float(np.dot(dual, demand)), 1.0)
    relative_gap = max(-min_reduced_cost, 0.0) / scale
    return VehicleColumnGenerationResult(
        request_table=requests,
        pattern_table=pattern_table,
        request_dual_price=pd.Series(dual, index=requests["request_id"].astype(str), name="service_dual_price"),
        average_service_price=pd.Series(average_price, index=requests["request_id"].astype(str), name="average_service_price"),
        road_link_flow=road_flow,
        supplied_capacity=supplied,
        demand_residual=residual,
        relative_pricing_gap=float(relative_gap),
        iterations=len(rows),
        pricing_calls=pricing_calls,
        history=pd.DataFrame(rows),
    )


